#include "Actor.h"
#include "../../Component/Base/Component.h"

//=============================================================================
// 更新関数
//=============================================================================
void Actor::Update()
{
	for (Component* component : m_Components)//所有コンポーネント更新
	{
		component->Update();
	}
}

//=============================================================================
// 終了関数
//=============================================================================
void Actor::Uninit()
{
	for (Component* component : m_Components)
	{
		component->Uninit();
		delete component;
		component = nullptr;
	}

	m_Components.clear(); //コンポーネントリストのクリア
}
