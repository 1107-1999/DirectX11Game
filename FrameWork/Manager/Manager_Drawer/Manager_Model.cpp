#include "Manager_Model.h"
#include "../Manager_Shader.h"
#include "../../Component/Component_Model/Base/Component_ModelBase.h"

//=============================================================================
// モデルコンポーネントリスト登録関数
//=============================================================================
void Manager_Model::AddComponent(class Component_ModelBase* _add)
{
	m_ComponentList.push_back(_add);
}

//=============================================================================
// 描画関数
//=============================================================================
void Manager_Model::Draw()
{
	//所有モデルコンポーネントを描画
	for (Component_ModelBase* component_Model : m_ComponentList)
	{
		component_Model->Draw();
	}
}

//=============================================================================
// 終了関数
//=============================================================================
void Manager_Model::Uninit()
{
	m_ComponentList.clear();//要素削除
}


